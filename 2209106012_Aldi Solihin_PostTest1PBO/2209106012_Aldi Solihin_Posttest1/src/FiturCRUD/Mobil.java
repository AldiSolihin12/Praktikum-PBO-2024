package FiturCRUD;

public class Mobil {
    String brand;
    String tipe;
    String warna;
    int harga;
    int tahun;

    public Mobil(String brand, String tipe, String warna, int tahun, int harga){
        this.brand = brand;
        this.tipe = tipe;
        this.warna = warna;
        this.tahun = tahun;
        this.harga = harga;
    }
}